app.post('/request', function(req, res, next){
  var message = req.body.Body;
  var from = req.body.From;
  console.log('From: ' + from + ', Message: ' + message);

  var options = {
    host: 'api.openweathermap.org',
    path: '/data/2.5/weather?zip='+message+',us&APPID=fcea7f4623776b63cfdc51b2fab81310'
  };

  callback = function(response)
  {
    var str = '';

    //another chunk of data has been recieved, so append it to `str`
    response.on('data', function (chunk) {
      str += chunk;
    });

    //the whole response has been recieved, so we just print it out here
    response.on('end', function () {
      console.log(str);
      // res.send(JSON.parse(str));
      var data = JSON.parse(str);
      var tID = data.weather[0].id;
      var tempu = data.main.temp;
      var main = data.weather[0].main;
      var fTemp = Math.floor((tempu*9/5) - 459.67);
      var comfortState = temperature_old(fTemp);

      var r = false;
      var s = false;
      var sun = false;

      if ((tID==800)||(tID==801))
      {
        sun = true;
      }
      if (Math.floor(tID/100)==6)
      {
        s = true;
      }
      if ((Math.floor(tID/100)==5)||(Math.floor(tID/100)==3)||(Math.floor(tID/100)==2))
      {
        r = true;
      }

      var w = w2w_old(comfortState, r, s, sun);

      client.messages.create({
          body: "Thanks! \n| "+ w + "\n| " + fTemp + "\n| " + main + "\n| " + tID,
          to: from,
          from: "+13313056064"
      }, function(err, message) {
          process.stdout.write(message.sid);
          console.log("\ndone\n");
      });
      res.write('done');
      res.end();
    });
  }

  http.request(options, callback).end();
});

app.use(express.compress());
app.use(express.favicon());
app.use(express.logger('dev'));
app.use(express.cookieParser());
app.use(express.json());
app.use(express.urlencoded());
app.use(expressValidator());
app.use(express.methodOverride());
app.use(express.session({
  secret: secrets.sessionSecret,
  store: new MongoStore({
    url: secrets.db,
    auto_reconnect: true
  })
}));
app.use(express.csrf());
app.use(passport.initialize());
app.use(passport.session());
app.use(function(req, res, next) {
  res.locals.user = req.user;
  res.locals.token = req.csrfToken();
  res.locals.secrets = secrets;
  next();
});
app.use(flash());
app.use(express.static(path.join(__dirname, 'public'), { maxAge: month }));
app.use(function(req, res, next) {
  // Keep track of previous URL
  if (req.method !== 'GET') return next();
  var path = req.path.split('/')[1];
  if (/(auth|login|logout|signup)$/.test(path)) return next();
  req.session.returnTo = req.path;
  next();
});
app.use(app.router);
app.use(function(req, res) {
  res.status(404);
  res.render('404');
});
app.use(express.errorHandler());

